class Employee{
  String name;
  String number;
  int id;
  double salary;
  
  public Employee(String name, String number, int id, double salary){
    this.name = name;
    this.number = number;
    this. id = id;
    this.salary = salary;
  }
  public Employee()
  {
    this.name = "";
    this.number = "";
    this. id = 0;
    this.salary = 0;
  }
}
